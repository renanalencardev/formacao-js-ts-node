/* 
    Boolean é um tipo de dado que representa um valor verdadeiro ou falso.
    Pode ser representado por true ou false.
*/

let verdadeiro = true;
let falso = false;

console.log(verdadeiro);
console.log(falso);

// Comparação de igualdade
console.log(1 === 1);
console.log(1 === 2);

// typeof retorna o tipo de dado de uma variável
console.log(typeof true);
console.log(typeof false);
