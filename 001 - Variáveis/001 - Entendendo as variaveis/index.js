// var - var é uma variável global
// let - let é uma variável local
// const - const é uma constante, também dentro de um escopo

// var é uma variável global, ou seja, pode ser acessada em qualquer parte do código
// let é uma variável local, ou seja, só pode ser acessada dentro do bloco onde foi declarada
// const é uma constante, ou seja, não pode ser alterada

// var é uma variável global, ou seja, pode ser acessada em qualquer parte do código
var nome = "João";
console.log(nome);

// let é uma variável local, ou seja, só pode ser acessada dentro do bloco onde foi declarada
let idade = 20;
console.log(idade);

// const é uma constante, ou seja, não pode ser alterada
const PI = 3.14;
console.log(PI);

