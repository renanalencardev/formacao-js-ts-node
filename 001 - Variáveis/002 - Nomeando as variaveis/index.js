/* 
    Camel Case, Pascal Case e Snake Case são diferentes convenções de nomenclatura utilizadas em programação 
    para unir palavras em identificadores (nomes de variáveis, funções, classes, etc.). Cada um tem suas próprias regras 
    de capitalização e separação de palavras. 
Camel Case:
    A primeira letra da primeira palavra é minúscula.
    A primeira letra de cada palavra subsequente é maiúscula.
    Exemplo: minhaVariavel, nomeDoUsuario.
    Usado comumente para nomes de variáveis e funções. 
Pascal Case:
    Também conhecido como Upper Camel Case.
    A primeira letra de todas as palavras é maiúscula.
    Exemplo: MinhaVariavel, NomeDoUsuario.
    Usado comumente para nomes de classes e tipos. 
Snake Case:
    Todas as palavras são escritas em letras minúsculas.
    As palavras são separadas por sublinhados (_).
    Exemplo: minha_variavel, nome_do_usuario.
    Usado comumente para nomes de variáveis, constantes e nomes de tabelas em banco de dados. 

    Em resumo, a principal diferença entre camel case e Pascal case está na capitalização da primeira letra 
    da primeira palavra. Já o snake case utiliza sublinhados para separar as palavras, enquanto camel case e Pascal case 
    as unem diretamente. 
*/
let nomeSobrenome = "João Silva"; // camelCase
