/*
    Atribuição
    O operador de atribuição é o sinal de igual (=). Ele atribui um valor a uma variável.
*/

let x = 10;
let y = 5;

console.log(x); // 10
console.log(y); // 5

// Atribuição de adição
x += y; // x = x + y => 10 + 5 
console.log(x); // 15

// Atribuição de subtração
x -= y; // x = x - y => 15 - 5
console.log(x); // 10

// Atribuição de multiplicação
x *= y; // x = x * y => 10 * 5
console.log(x); // 50

// Atribuição de divisão
x /= y; // x = x / y => 50 / 5
console.log(x); // 10

