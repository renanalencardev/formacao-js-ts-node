/*
    Remainder (%) - Resto da divisão
    O operador de resto da divisão (%) retorna o resto da divisão de dois números.
    Se o resto da divisão for 0, significa que o número é par.
    Se o resto da divisão for 1, significa que o número é ímpar.
*/

console.log(10 % 3); // 1
console.log(4 % 2); // 0

// Verificando se um número é par ou ímpar
const numero = 10;  // Par
console.log(numero % 2 === 0); // true 
